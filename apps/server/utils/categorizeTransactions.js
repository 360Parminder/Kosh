function categorizeTransactions(data) {

  const subscriptionKeywords = [
        'netflix', 'godaddy', 'healthplix', 'popclub', 'mobikwik', 'quickcart', 'razorpay software', 'jiosaavn', 'spotify'
    ];
    const billKeywords = [
        'bbps', 'bijli', 'electric', 'water', 'broadband', 'reliance', 'bsnl', 'vi recharge', 'airtel', 'postpaid', 'bbpsbp', 'billdesk'
    ];
    const refundKeywords = ['upi/rev/', 'rev', 'reversed'];
    const merchantKeywords = ['flipkart', 'amazon', 'paytm', 'bigbasket', 'swiggy', 'zomato'];


  // Extract transactions from the data
  const transactions = data.fiData[0].data[0].decryptedFI.account.transactions.transaction;
  
  // Initialize categorized transactions
  const categorized = {
    subscriptions: [],
    bills: [],
    refunds: [],
    merchants: [],
    others: []
  };

  // Helper function to check if any keyword exists in the narration
  const containsKeyword = (narration, keywords) => {
    const lowerNarration = narration.toLowerCase();
    return keywords.some(keyword => lowerNarration.includes(keyword.toLowerCase()));
  };

  // Categorize each transaction
  transactions.forEach(txn => {
    const narration = txn.narration.toLowerCase();
    
    if (containsKeyword(narration, refundKeywords)) {
      categorized.refunds.push(txn);
    } 
    else if (containsKeyword(narration, subscriptionKeywords)) {
      categorized.subscriptions.push(txn);
    }
    else if (containsKeyword(narration, billKeywords)) {
      categorized.bills.push(txn);
    }
    else if (containsKeyword(narration, merchantKeywords)) {
      categorized.merchants.push(txn);
    }
    else {
      categorized.others.push(txn);
    }
  });

  return categorized;
}
